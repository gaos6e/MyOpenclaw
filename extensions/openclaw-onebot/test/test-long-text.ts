import { markdownToImage } from "../src/og-image.js";

const md = `我能发送长文字。以下是一段关于人工智能发展的文字，超过500字：

人工智能的发展历程可以追溯到上世纪中叶。1956年的达特茅斯会议被认为是人工智能诞生的标志，当时一群年轻的科学家聚集在一起，讨论如何让机器模拟人类智能。从那时到现在，AI经历了几次寒冬和复苏，每一次技术突破都带来了新的可能性。

早期的AI研究主要集中在符号推理和专家系统上。研究人员试图通过编写大量的规则来让计算机具备人类的知识和推理能力。这种方法在特定领域取得了一些成功，比如医疗诊断和棋类游戏，但很快就遇到了瓶颈。因为人类的知识太过复杂，很难用简单的规则来完全描述。

进入21世纪，随着互联网的普及和计算能力的提升，机器学习方法开始崭露头角。特别是深度学习的出现，彻底改变了AI的发展轨迹。2012年，AlexNet在ImageNet图像识别竞赛中取得了突破性成绩，证明了深度神经网络在视觉任务上的强大能力。此后，深度学习在语音识别、自然语言处理、自动驾驶等领域都取得了巨大进展。

最近几年，大语言模型的出现标志着AI进入了一个新的阶段。以GPT系列为代表的模型展现了惊人的语言理解和生成能力，能够进行对话、写作、编程甚至创造性思考。这些模型通过在海量文本数据上进行预训练，学习到了丰富的语言知识和世界知识。虽然它们仍然存在幻觉、偏见等问题，但已经展现出了接近甚至超越人类在某些任务上的表现。

展望未来，人工智能将继续深入我们生活的方方面面。从智能家居到医疗诊断，从教育辅助到科学研究，AI都将成为重要的工具。同时，我们也需要认真思考AI带来的伦理和社会问题，确保这项技术的发展能够真正造福人类。技术的进步不应该只是追求性能的提升，更要考虑如何让每个人都能从中受益，如何保护个人隐私和数据安全，如何避免技术被滥用。这需要技术专家、政策制定者和整个社会共同参与，建立一个健康、可持续的AI发展生态。`;

async function main() {
  console.log("生成图片...");
  const result = await markdownToImage(md, { theme: "dust" });
  console.log("结果:", result);
  
  if (result) {
    // 复制到测试目录方便查看
    const { copyFileSync } = await import("fs");
    const path = result.replace("file://", "");
    copyFileSync(path, "./test/output-long-text.png");
    console.log("图片已保存到: ./test/output-long-text.png");
  }
}

main();
